package com.example.alex.fitofan.models;

public class WallModel {
    private String firstName;
    private String lastName;
    private String imageUser;
    private String namePlan;
    private String datePublication;
    private String imageTrainig;


    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getImageUser() {
        return imageUser;
    }

    public void setImageUser(String imageUser) {
        this.imageUser = imageUser;
    }

    public String getNamePlan() {
        return namePlan;
    }

    public void setNamePlan(String namePlan) {
        this.namePlan = namePlan;
    }

    public String getDatePublication() {
        return datePublication;
    }

    public void setDatePublication(String datePublication) {
        this.datePublication = datePublication;
    }

    public String getImageTrainig() {
        return imageTrainig;
    }

    public void setImageTrainig(String imageTrainig) {
        this.imageTrainig = imageTrainig;
    }
}
