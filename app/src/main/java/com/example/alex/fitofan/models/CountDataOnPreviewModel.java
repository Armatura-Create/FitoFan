package com.example.alex.fitofan.models;

public class CountDataOnPreviewModel {
    private String time;
    private String distance;
    private String distanceParam;
    private String count;
    private String weight;
    private String weightParam;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getDistanceParam() {
        return distanceParam;
    }

    public void setDistanceParam(String distanceParam) {
        this.distanceParam = distanceParam;
    }

    public String getWeightParam() {
        return weightParam;
    }

    public void setWeightParam(String weightParam) {
        this.weightParam = weightParam;
    }
}
